#import "QCloudIntelligentTieringConfiguration.h"

#import "QCloudIntelligentTieringTransition.h"

NS_ASSUME_NONNULL_BEGIN
@implementation QCloudIntelligentTieringConfiguration

+ (NSDictionary *)modelCustomPropertyMapper {
    return @{
        @"status" : @"Status",
        @"transition" : @"Transition",
    };
}

- (BOOL)modelCustomTransformToDictionary:(NSMutableDictionary *)dic {
    NSNumber *status = dic[@"Status"];
    if (status) {
        NSString *value = QCloudIntelligentTieringStatusTransferToString([status intValue]);
        if (value) {
            dic[@"Status"] = value;
        }
    }

    return YES;
}

- (NSDictionary *)modelCustomWillTransformFromDictionary:(NSDictionary *)dic {
    if (!dic) {
        return dic;
    }
    NSMutableDictionary *transfromDic = [NSMutableDictionary dictionaryWithDictionary:dic];

    NSString *status = transfromDic[@"Status"];
    if (status && [status isKindOfClass:[NSString class]]
        && status.length > 0) {
        int value = QCloudIntelligentTieringStatusDumpFromString(status);
        transfromDic[@"Status"] = @(value);
    }
    return transfromDic;
}

@end

NS_ASSUME_NONNULL_END
